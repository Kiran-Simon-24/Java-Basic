package basic.java;
public class operators {
  public static void main(String[] args){
    int x = 10;
    int y = -10;
    boolean a = true;
    boolean b = false;
    System.out.println(x++); // 11
    System.out.println(++x); // 12
    //System.out.println(x--);
    System.out.println(!a);
    System.out.println(!b);
    System.out.println(~x);
    System.out.println(~y);
    System.out.println(--y);
  }  
}
